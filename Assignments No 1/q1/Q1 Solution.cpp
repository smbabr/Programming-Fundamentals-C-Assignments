#include <stdio.h>

int main() {
    int num;
    
    // Let's make it fun! Ask for a number like a magician!
    printf("Enter a number to see its magical transformation: ");
    
    // Taking input using scanf
    scanf("%d", &num);
    
    // Calculating the square of the number in a creative way
    int square = num * num;
    
    // Displaying the result with a little bit of fun!
    printf("Behold! The square of %d is %d. Magic, isn't it?\n", num, square);
    
    return 0;
}

